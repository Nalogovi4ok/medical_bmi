def calculate_bmr():
    """
    Рассчитывает Базальный Метаболизм (BMR) по формуле Миффлина-Сан Жеора.
    BMR - это количество калорий, необходимых для жизнедеятельности в состоянии покоя.
    """
    try:
        weight = float(input("Введите ваш вес в кг: "))
        height = float(input("Введите ваш рост в см: "))
        age = int(input("Введите ваш возраст: "))
        
        if weight <= 0 or height <= 0 or age <= 0:
            print("Ошибка! Все значения должны быть положительными.")
            return
            
        gender = input("Введите ваш пол (м/ж): ").lower().strip()

        # Формула Миффлина-Сан Жеора
        if gender in ['м', 'муж', 'мужской', 'm', 'male']:
            bmr = (10 * weight) + (6.25 * height) - (5 * age) + 5
        elif gender in ['ж', 'жен', 'женский', 'f', 'female']:
            bmr = (10 * weight) + (6.25 * height) - (5 * age) - 161
        else:
            print("Ошибка! Пол не распознан. Используйте 'м' или 'ж'.")
            return

        print(f"\nВаш Базальный Метаболизм (BMR): {bmr:.2f} ккал/день.")
        print("*Это количество калорий, которое ваш организм тратит в состоянии полного покоя.*")

    except ValueError:
        print("Ошибка! Пожалуйста, вводите числа правильно.")

# Запуск функции
if __name__ == "__main__":

    calculate_bmr()
