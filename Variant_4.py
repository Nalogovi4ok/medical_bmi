def calculate_whr():
    """
    Рассчитывает Индекс Талия/Бедро (WHR) и оценивает риски для здоровья.
    Формула: обхват талии (см) / обхват бедер (см)
    """
    try:
        waist = float(input("Введите обхват вашей талии (в см): "))
        hips = float(input("Введите обхват ваших бедер (в см): "))
        
        if waist <= 0 or hips <= 0:
            print("Ошибка! Значения должны быть положительными.")
            return
            
        if hips == 0:
            print("Ошибка! Обхват бедер не может быть нулевым.")
            return

        whr = waist / hips
        print(f"\nВаш Индекс Талия/Бедро (WHR): {whr:.2f}")

        # Оценка рисков (для взрослых)
        gender = input("Введите ваш пол (м/ж): ").lower().strip()
        
        if gender in ['м', 'муж', 'мужской', 'm', 'male']:
            if whr < 0.9:
                risk = "Низкий риск здоровья"
            elif whr < 1.0:
                risk = "Умеренный риск здоровья"
            else:
                risk = "Высокий риск здоровья (абдоминальное ожирение)"
        elif gender in ['ж', 'жен', 'женский', 'f', 'female']:
            if whr < 0.8:
                risk = "Низкий риск здоровья"
            elif whr < 0.85:
                risk = "Умеренный риск здоровья"
            else:
                risk = "Высокий риск здоровья (абдоминальное ожирение)"
        else:
            risk = "Неизвестный пол. Оценка невозможна."

        print(f"Оценка рисков: {risk}")

    except ValueError:
        print("Ошибка! Пожалуйста, вводите только числа.")

# Запуск функции
if __name__ == "__main__":
    calculate_whr()